//control statements
//if stmt

let a=10;
let b=20;
if ((b<a)){
    console.log('b is less than a');
}

else{
    console.log('b is greater than a')
} 

//for loop
console.log('\nfor loop')
for(let i=5; i>0; i--){
    console.log(i)
}

//while loop
console.log('\nwhile loop')
let c=5;
while(c>0){
    console.log('val of c is: ',c);
    c--;
}

//do while loop
console.log('\ndo while loop')
let d=2;
do{
    console.log('val of d is: ',d);
    d--;
}
while(d>0);